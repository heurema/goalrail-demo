export type AppLocale = "en" | "ru";

export const getLocaleFromPath = (pathname: string): AppLocale =>
  pathname === "/ru" || pathname.startsWith("/ru/") ? "ru" : "en";

export const getPathForLocale = (locale: AppLocale): string =>
  locale === "ru" ? "/ru" : "/";

export const getIntlLocale = (locale: AppLocale): string =>
  locale === "ru" ? "ru-RU" : "en-US";

